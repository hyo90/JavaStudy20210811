//INTERFACE로 만듦

package b15_인터페이스;

/**
 * 
 *  인터페이스(interface)
 *  약속에 대한 정의를 하는 것
 * 상속이 아니라 구현이라 한다. (implement)
 * 상속 = 다중상속 x
 * 구현 = 다중구현 o
 * 
 * 기본적으로 모든 메소즈가 추상(abstract)메소드이다.
 * 
 *  public (Abstract) interface 생략되어있음
 * 
 */

public interface Switch {
	public void on();
	public void off();
	

}
